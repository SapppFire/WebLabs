                <div class="how-fermer">
                    <h5 class = "fermer-h5">Как стать фермером и стоит ли это делать</h5>
                    <p class = "par-1">
                    У загородной жизни самые разные формы, для одних, это дача для шашлыка по выходным, для других, 
                    возможность завести десяток курочек на яйцо, для третьих, способ заработать на жизнь. 
                    Когда речь именно о заработке, актуальны фермерские хозяйства, точнее, крестьянские фермерские хозяйства. 
                    Они появились еще в Советском Союзе в конце восьмидесятых, начале девяностых, как один из первых видов предпринимательской деятельности. 
                    Но полноценный закон, регламентирующий создание фермерского хозяйства, разработали уже в России, в начале двухтысячных. 
                    В статье рассмотрим основные аспекты, касающиеся этих организаций.
                    </p>
