                    <h3 style="padding-top: 20px; text-align: left; font-weight: bold;">Что собой представляет крестьянское фермерское хозяйство</h3>

                    <p class = "par-1">
                    Крестьянское фермерское хозяйство (КФХ), это добровольное объединение людей, связанных какими-либо родственными или 
                    свойскими узами и имеющих в общей собственности определенное имущество. Организуется КФХ или ФХ (такая трактовка допускается законодательством) 
                    с целью ведения производственной и сопутствующей деятельности. Фермерское хозяйство, это коммерческая организация, 
                    ориентированная на получение прибыли путем реализации произведенной сельскохозяйственной продукции либо результата ее переработки. 
                    Одним из главных условий ведения фермерского хозяйства является личное участие всех членов в производственном цикле. 
                    Правом на создание КФХ обладает любой дееспособный человек, как имеющий российское гражданство, так и без него или подданный другого государства. 
                    Естественно и все члены должны быть дееспособными и соответствовать другим требованиям.
                    </p>

                    <ul style="text-align: left; font-size: 21px;">
                        <li>В фермерское хозяйство могут войти: супруги, их дети и внуки, а также родители, бабушки и дедушки, братья и сестры каждого из супругов.</li>
                        <li>В рамках КФХ может быть объединено не более трех семей.</li>
                        <li>Членство возможно только с 16 лет.
                        Состоять можно только в одном фермерском хозяйстве.</li>
                        <li>Допускается участие в деятельности хозяйства посторонних лиц, но не больше пяти человек.</li>
                    </ul>
